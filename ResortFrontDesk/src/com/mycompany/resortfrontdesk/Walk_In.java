private int calculateTotalCost() {
    int baseCost = 0;

    // Customize the room rates based on your requirements
    if (daytimeCheckBox.isSelected()) {
        baseCost += 50; 
    }

    if (nighttimeCheckBox.isSelected()) {
        baseCost += 80; 
    }

    int adultRate = 150;
    int kidRate = 60;

    int numAdults = adultratingsBox.isSelected() ? 1 : 0;
    int numKids = kidratingsBox.isSelected() ? 1 : 0;

    baseCost += (numAdults * adultRate) + (numKids * kidRate);

    // Apply 5 percent discount for senior citizens
    if (cbSeniorCitizen.isSelected()) {
        int seniorDiscount = (int) (baseCost * 0.05);
        baseCost -= seniorDiscount;
    }

    // Apply 5 percent discount for PWDs
    if (cbPWD.isSelected()) {
        int pwdDiscount = (int) (baseCost * 0.05);
        baseCost -= pwdDiscount;
    }

    return baseCost;
}

    private void updateTotalCost() {
    int totalCost = calculateTotalCost();
    walkinPriceLabel.setText("PRICE: PHP " + totalCost);
}
    
    


    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        try {
            int totalCost = calculateTotalCost();
            
            String sql = "INSERT INTO guestwalkin (name, phnum, address, isadultratings, iskidratings, daytimeswim, nighttimeswim, isSeniorCitizen, isPWD, totalCost) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            pst = con.prepareStatement(sql);
            
            pst.setString(1,guestName.getText());
            pst.setString(2,guestPhNum.getText());
            pst.setString(3, guestAddress.getText());
            pst.setBoolean(4, adultratingsBox.isSelected());
            pst.setBoolean(5, kidratingsBox.isSelected());
            pst.setBoolean(6, daytimeCheckBox.isSelected());
            pst.setBoolean(7, nighttimeCheckBox.isSelected());
            pst.setBoolean(8, cbSeniorCitizen.isSelected());
            pst.setBoolean(9, cbPWD.isSelected());
            pst.setInt(10, totalCost);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Walk-In recorded successfully. Total Cost: PHP" + totalCost);
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Walk_In.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error recording walk-in. Please try again.");
        }
        dispose();
        ResortFrontDesk home1 = new ResortFrontDesk();
        home1.setVisible(true);
    
    }//GEN-LAST:event_SubmitButtonActionPerformed

